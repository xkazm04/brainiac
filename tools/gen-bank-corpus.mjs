/*
 * Generate fixtures/bank — the scale corpus. See docs/BANK-CORPUS.md.
 *
 * Deterministic by construction: every choice is a hash of a stable key, never
 * Math.random(), so re-running produces byte-identical YAML. A diff here means
 * the generator changed.
 *
 *   node tools/gen-bank-corpus.mjs
 *   cargo run -p brainiac-server -- fixtures lint --fixtures fixtures/bank
 */

import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

import {
  CLAIMS,
  HALF_LIFE,
  HUBS,
  h01,
  hInt,
  pick,
  PRACTICES,
  SEAMS,
  SERVICES,
  VARIANTS,
  CONCEPTS,
  TEAMS,
} from "./bank-org.mjs";

const ROOT = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const OUT = path.join(ROOT, "fixtures", "bank");

const TARGET_MEMORIES = 1200;
/** The corpus's clock. Fixed, so valid_from/valid_to never drift with the run. */
const NOW = new Date("2026-07-15T00:00:00Z");
const iso = (d) => new Date(d).toISOString().replace(".000Z", "Z");
const daysBefore = (n) => iso(NOW.getTime() - n * 86400000);
const daysAfter = (n) => iso(NOW.getTime() + n * 86400000);

// ── YAML emitter ─────────────────────────────────────────────────────────
// Hand-rolled because the repo has no JS yaml dep and this needs exactly one
// shape. Quotes every string: the corpus is full of colons and quotes, and a
// bare scalar that happens to parse as a time is a debugging afternoon.
const q = (s) => `"${String(s).replace(/\\/g, "\\\\").replace(/"/g, '\\"')}"`;
const list = (xs) => `[${xs.map(q).join(", ")}]`;

function emit(lines) {
  return lines.filter((l) => l !== null).join("\n") + "\n";
}

// ── identity ─────────────────────────────────────────────────────────────
const users = [];
for (const t of TEAMS) {
  const short = t.name.replace(/-/g, "");
  users.push({ id: `user-${short}-lead`, email: `${short}lead@meridianbank.example`, teams: [t.id], role: "maintainer" });
  users.push({ id: `user-${short}-dev1`, email: `${short}dev1@meridianbank.example`, teams: [t.id], role: "member" });
  users.push({ id: `user-${short}-dev2`, email: `${short}dev2@meridianbank.example`, teams: [t.id], role: "member" });
}

const orgYaml = emit([
  "# Meridian Bank — the scale corpus's identity. GENERATED by",
  "# tools/gen-bank-corpus.mjs; see docs/BANK-CORPUS.md §1. Do not hand-edit.",
  "org: meridian-bank",
  "teams:",
  ...TEAMS.map((t) => `  - id: ${t.id}\n    name: ${t.name}`),
  "users:",
  ...users.map((u) => `  - id: ${u.id}\n    email: ${u.email}\n    teams: [${u.teams.join(", ")}]\n    role: ${u.role}`),
]);

// ── entities ─────────────────────────────────────────────────────────────
// Two populations, per BANK-CORPUS §2.2: five hubs every team names its own way,
// and a long tail each owned by exactly one team.
const entities = [];
const mergeSets = [];

for (const hub of HUBS) {
  const members = [];
  for (const [team, names] of Object.entries(hub.dialects)) {
    for (const [i, name] of names.entries()) {
      const id = `ent-${hub.id}-${team.replace("team-", "")}-${i}`;
      entities.push({ id, team, name, kind: hub.kind, aliases: [] });
      members.push(id);
    }
  }
  mergeSets.push({ canonical: hub.canonical, kind: hub.kind, difficulty: "hard", members });
}

const tailByTeam = new Map();
for (const t of TEAMS) {
  const pool = SERVICES[t.domain] ?? SERVICES.platform;
  const own = [];

  for (const svc of pool) {
    // Regional / legacy deployments — the reason a real graph has hundreds of
    // nodes (see VARIANTS in bank-org.mjs). Most services have one; the busy
    // ones have several, and `-legacy` only exists where a migration stalled.
    const spread = h01(`${t.id}:${svc}`, 61);
    const count = spread > 0.82 ? 4 : spread > 0.55 ? 2 : 1;
    const variants = VARIANTS.slice(0, count);
    const members = [];
    for (const v of variants) {
      const name = `${svc}${v}`;
      const id = `ent-${t.name}-${name}`;
      entities.push({ id, team: t.id, name, kind: "service", aliases: [] });
      own.push({ id, name });
      members.push(id);
    }
    // Every deployment of one service is the same service. Binding them is the
    // graph's whole job, so the variants are one canonical node, not four.
    mergeSets.push({
      canonical: svc,
      kind: "service",
      difficulty: members.length > 1 ? "medium" : "easy",
      members,
    });
  }

  for (const c of CONCEPTS[t.domain] ?? CONCEPTS.platform) {
    const id = `ent-${t.name}-${c.replace(/\s+/g, "-")}`;
    entities.push({ id, team: t.id, name: c, kind: "concept", aliases: [] });
    own.push({ id, name: c });
    if (h01(id, 62) < 0.6) {
      mergeSets.push({ canonical: c, kind: "concept", difficulty: "easy", members: [id] });
    }
  }

  tailByTeam.set(t.id, own);
}

/*
 * Negative pairs: entities that must NOT merge.
 *
 * The first attempt paired each team's first service with another team's, which
 * produced (security/feature-store, data/feature-store) — and those two ARE the
 * same service, bound by the `feature-store` merge set two blocks up. The
 * validator caught the gold contradicting itself. Two teams sharing a domain
 * share a service pool, so the pair is only negative when the NAMES differ.
 */
const canonicalOf = new Map();
for (const m of mergeSets) for (const id of m.members) canonicalOf.set(id, m.canonical);

const negativePairs = [];
for (let i = 0; i < TEAMS.length && negativePairs.length < 12; i++) {
  const a = TEAMS[i % TEAMS.length];
  const b = TEAMS[(i + 5) % TEAMS.length];
  const ea = tailByTeam.get(a.id).find(
    (x) => !tailByTeam.get(b.id).some((y) => canonicalOf.get(x.id) === canonicalOf.get(y.id)),
  );
  const eb = tailByTeam.get(b.id).find(
    (y) => ea && canonicalOf.get(y.id) !== canonicalOf.get(ea.id),
  );
  if (ea && eb && ea.id !== eb.id) negativePairs.push([ea.id, eb.id]);
}

const entitiesYaml = emit([
  "# GENERATED by tools/gen-bank-corpus.mjs — see docs/BANK-CORPUS.md §2.2.",
  "entities:",
  ...entities.map(
    (e) =>
      `  - id: ${e.id}\n    team: ${e.team}\n    name: ${q(e.name)}\n    kind: ${e.kind}` +
      (e.aliases.length ? `\n    aliases: ${list(e.aliases)}` : ""),
  ),
]);

const mergesYaml = emit([
  "# GENERATED. merge_sets bind each team's dialect for a hub onto one canonical",
  "# node; negative_pairs are the ones that must stay apart.",
  "merge_sets:",
  ...mergeSets.map(
    (m) =>
      `  - canonical: ${q(m.canonical)}\n    kind: ${m.kind}\n    difficulty: ${m.difficulty}\n    members: [${m.members.join(", ")}]`,
  ),
  "negative_pairs:",
  ...negativePairs.map((p) => `  - [${p[0]}, ${p[1]}]`),
]);

// ── memories ─────────────────────────────────────────────────────────────
const memories = [];
const totalWeight = TEAMS.reduce((s, t) => s + t.weight, 0);
let seq = 0;

/** Visibility pyramid (§2.4). Hub-anchored memories skew org-visible: they ARE
 *  the cross-team contracts. */
function visibilityFor(seed, onHub) {
  const r = h01(seed, 21);
  if (r < 0.02) return "private";
  if (onHub) return r < 0.62 ? "org" : "team";
  return r < 0.2 ? "org" : "team";
}

/** Status mix (§2.5). */
function statusFor(seed) {
  const r = h01(seed, 22);
  if (r < 0.05) return "raw";
  if (r < 0.13) return "candidate";
  return "canonical";
}

for (const t of TEAMS) {
  const n = Math.round((t.weight / totalWeight) * TARGET_MEMORIES);
  const tail = tailByTeam.get(t.id);
  const hubEnts = entities.filter((e) => e.team === t.id && e.id.startsWith("ent-hub-"));
  const claims = CLAIMS[t.domain] ?? CLAIMS.platform;

  for (let i = 0; i < n; i++) {
    seq++;
    const id = `mem-${t.name}-${String(i).padStart(4, "0")}`;
    const onHub = hubEnts.length > 0 && h01(id, 3) < 0.3;
    const anchor = onHub ? pick(hubEnts, id, 4) : pick(tail, id, 5);
    const [kind, titleTpl, tpl] = pick(claims, id, 6);
    const content = tpl.replace(/X/g, anchor.name);
    const title = titleTpl.replace(/X/g, anchor.name);
    const born = 20 + Math.floor(h01(id, 8) * 900);
    const life = HALF_LIFE[kind] ?? 400;
    const status = statusFor(id);
    const vis = visibilityFor(id, onHub);

    memories.push({
      id,
      team: t.id,
      visibility: vis,
      owner: vis === "private" ? `user-${t.name.replace(/-/g, "")}-dev1` : null,
      status,
      kind: kind === "policy" ? "fact" : kind, // policy is a flavour, not a kind
      title,
      content,
      entities: [anchor.id],
      valid_from: daysBefore(born),
      // Live memories carry a TTL into the future — the thing that broke the
      // archive's playhead. The corpus must keep reproducing it.
      valid_to: daysAfter(Math.max(30, life - born + Math.floor(h01(id, 9) * 200))),
      source: null,
    });
  }
}

// Supersession chains (§3): ~90 pairs, so the archive has something to resurrect.
const daysAgoOf = (isoStr) => Math.round((NOW.getTime() - new Date(isoStr).getTime()) / 86400000);

let chains = 0;
for (const m of memories) {
  if (chains >= 90) break;
  if (m.status !== "canonical" || h01(m.id, 11) > 0.09) continue;
  // The flip must land INSIDE the memory's life. Picking it from a flat range
  // dated some memories' supersession before their own creation — an inverted
  // validity window, which validate.rs caught as an as-of case expecting an
  // answer the corpus could never give. It is superseded somewhere in the back
  // half of its life, never before it existed.
  const born = daysAgoOf(m.valid_from);
  if (born < 30) continue; // too young to have been revised yet
  const flipDaysAgo = Math.max(5, Math.floor(born * (0.15 + h01(m.id, 12) * 0.5)));
  const newId = `${m.id}-v2`;
  m.status = "deprecated";
  m.valid_to = daysBefore(flipDaysAgo);
  m.superseded_by = newId;
  memories.push({
    id: newId,
    team: m.team,
    visibility: m.visibility,
    // A private memory's successor is still private, and private means owned by
    // a person — inheriting the visibility without the owner produced an
    // ownerless private memory, which is a row RLS cannot scope to anybody.
    owner: m.owner,
    status: "canonical",
    kind: "decision",
    title: m.title ? `${m.title} (revised)` : null,
    content: `${m.content} — superseded: this was revised at the ${pick(["Q1", "Q2", "Q3", "Q4"], newId, 13)} architecture review`,
    entities: m.entities,
    valid_from: daysBefore(flipDaysAgo),
    valid_to: daysAfter(200 + Math.floor(h01(newId, 14) * 500)),
    source: null,
  });
  chains++;
}

// Practice divergence substrate (§2.7): every team's own answer to each
// cross-cutting practice. These are the memories a sweep would cluster.
for (const t of TEAMS) {
  for (const p of PRACTICES) {
    const id = `mem-${t.name}-prac-${p.id}`;
    const variant = pick(p.variants, `${t.id}:${p.id}`, 31);
    memories.push({
      id,
      team: t.id,
      visibility: "org",
      owner: null,
      status: "canonical",
      kind: "decision",
      title: `${p.practice} — ${t.name}`,
      content: `${p.practice} in ${t.name}: ${variant}`,
      entities: [tailByTeam.get(t.id)[0].id],
      valid_from: daysBefore(100 + Math.floor(h01(id, 32) * 500)),
      valid_to: daysAfter(300),
      source: null,
    });
  }
}

// Contradiction pairs on the seams (§2.6). Generated LAST so their ids are
// stable regardless of how the bulk above is tuned.
const contraCases = [];
for (const [i, seam] of SEAMS.entries()) {
  const perSeam = 6;
  for (let k = 0; k < perSeam; k++) {
    const base = `seam-${i}-${k}`;
    const aId = `mem-seam-${i}-${k}-a`;
    const bId = `mem-seam-${i}-${k}-b`;
    const hub = HUBS.find((x) => x.id === seam.hub);
    const entA = entities.find((e) => e.team === seam.a && e.id.startsWith(`ent-${seam.hub}-`));
    const entB = entities.find((e) => e.team === seam.b && e.id.startsWith(`ent-${seam.hub}-`));
    const teamA = TEAMS.find((t) => t.id === seam.a);
    const teamB = TEAMS.find((t) => t.id === seam.b);
    const older = 200 + hInt(base, 300, 41);
    // The gold for a supersede case must already encode the winning pointer
    // (validate.rs `contradiction-direction`), and the loser has to look like a
    // loser: superseded_by set, status deprecated, validity closed at the moment
    // the winner opened. Anything less and the fixture asserts an outcome the
    // corpus itself contradicts.
    const supersedes = k < 4;
    const flipAt = older - 60;
    memories.push({
      id: aId,
      team: seam.a,
      visibility: "org",
      owner: null,
      status: supersedes ? "deprecated" : "canonical",
      kind: "decision",
      title: `${seam.topic} — ${teamA.name}`,
      content: `${seam.topic}: ${teamA.name} holds that ${pick(["the authoritative moment is when the instruction is accepted", "the earlier record wins and later corrections are compensating entries", "the owning team's decision stands until formally revoked", "the check runs once, at the point of entry"], base, 42)}`,
      entities: entA ? [entA.id] : [],
      valid_from: daysBefore(older),
      valid_to: supersedes ? daysBefore(flipAt) : daysAfter(200),
      superseded_by: supersedes ? bId : null,
      source: null,
    });
    memories.push({
      id: bId,
      team: seam.b,
      visibility: "org",
      owner: null,
      status: "canonical",
      kind: "decision",
      title: `${seam.topic} — ${teamB.name}`,
      content: `${seam.topic}: ${teamB.name} holds that ${pick(["the authoritative moment is when funds actually move", "the latest record wins outright and supersedes the earlier one", "the control function's decision overrides the owning team's", "the check must run again at settlement, because the inputs move"], base, 43)}`,
      entities: entB ? [entB.id] : [],
      valid_from: daysBefore(flipAt),
      valid_to: daysAfter(200),
      source: null,
    });
    // The gold: 4 of 6 per seam are genuine supersessions; the rest are the
    // traps (§2.6 — a detector that flags every tension trains reviewers to
    // ignore the queue).
    const expected = k < 4 ? "resolved_supersede" : k === 4 ? "resolved_coexist" : "dismissed";
    contraCases.push({
      id: `con-${i}-${k}`,
      memory_a: aId,
      memory_b: bId,
      expected,
      direction: expected === "resolved_supersede" ? "b_over_a" : null,
      hub,
    });
  }
}

/*
 * Pick each team's transcript-gold memory and link it, BEFORE the YAML is
 * emitted. The link is bidirectional (validate.rs `transcript-gold`): the
 * transcript claims the memory as its extraction gold, so the memory has to name
 * the transcript back — otherwise provenance depends on which end you read from.
 */
const transcriptGold = new Map();
for (const t of TEAMS) {
  const m = memories.find(
    (x) => x.team === t.id && x.status === "canonical" && x.visibility === "org",
  );
  if (!m) continue;
  m.source = `src-${t.name}-001`;
  transcriptGold.set(t.id, m);
}

