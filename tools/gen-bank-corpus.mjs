// Temporal: the supersession chains are the only honest as-of cases.
const byId = new Map(memories.map((m) => [m.id, m]));
const chainPairs = memories.filter((m) => m.superseded_by && byId.has(m.superseded_by)).slice(0, 12);
/**
 * The midpoint of a memory's own validity window.
 *
 * Not "the flip minus 30 days", which is what this did first and what the
 * validator caught: a chain whose old memory only lived 20 days has no valid
 * instant 30 days before it closed, and the case asserted an answer the corpus
 * could not give. The midpoint is inside the window by construction.
 */
const midpoint = (m) => {
  const from = new Date(m.valid_from).getTime();
  const to = m.valid_to ? new Date(m.valid_to).getTime() : NOW.getTime();
  return iso(from + (to - from) / 2);
};

const asofYaml = emit([
  "# GENERATED from the supersession chains: at the old memory's midpoint IT is",
  "# the answer; at the new one's midpoint the new one is. Both instants sit",
  "# inside their memory's validity window by construction.",
  "cases:",
  ...chainPairs.flatMap((old, i) => {
    const next = byId.get(old.superseded_by);
    return [
      `  - id: tmp-${String(i * 2 + 1).padStart(3, "0")}\n    question: ${q(old.content.slice(0, 70))}\n    as_of: ${q(midpoint(old))}\n    expected_memory: ${old.id}`,
      `  - id: tmp-${String(i * 2 + 2).padStart(3, "0")}\n    question: ${q(old.content.slice(0, 70))}\n    as_of: ${q(midpoint(next))}\n    expected_memory: ${next.id}`,
    ];
  }),
]);

// Transcripts: extraction gold. One per team — enough to declare the profile,
// small enough to stay honest about being generated.
const transcripts = TEAMS.filter((t) => transcriptGold.has(t.id)).map((t) => {
  const m = transcriptGold.get(t.id);
  const id = m.source;
  return {
    file: `${id}.yaml`,
    body: emit([
      `# GENERATED extraction gold for ${t.name}.`,
      `id: ${id}`,
      `team: ${t.id}`,
      "kind: session_transcript",
      "language: en",
      "turns:",
      `  - role: user\n    text: ${q(`We hit this again in ${t.name} today. Can you write down what we concluded so the next person does not rediscover it?`)}`,
      `  - role: assistant\n    text: ${q(m.content)}`,
      `  - role: user\n    text: ${q("Yes — that is exactly it. Capture that.")}`,
      "gold_memories:",
      // The gold id must BE a memory in gold.yaml (validate.rs `unknown-memory`):
      // extraction gold says "this transcript should yield THAT memory", so an
      // invented id asserts a claim about a memory that does not exist.
      `  - id: ${m.id}\n    kind: ${m.kind}\n    content_gist: ${q(m.content.slice(0, 80))}\n    entities: [${m.entities.join(", ")}]\n    visibility: org`,
      "distractors: []",
    ]),
  };
});

